    private void studentsMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_studentsMouseClicked
        try {
            DefaultTableModel d = (DefaultTableModel)students.getModel();
            int Index=students.getSelectedRow();
            
            f_name.setText(d.getValueAt(Index, 1).toString());
            l_name.setText(d.getValueAt(Index, 2).toString());
            Date date=new SimpleDateFormat("yyyy-MM-dd").parse(d.getValueAt(Index, 3).toString());
            dob.setDate(date);
            gender.setSelectedItem(d.getValueAt(Index, 4).toString());
            nic.setText(d.getValueAt(Index, 5).toString());
            address.setText(d.getValueAt(Index, 6).toString());
            
            
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, e);
        }
    }//GEN-LAST:event_studentsMouseClicked


