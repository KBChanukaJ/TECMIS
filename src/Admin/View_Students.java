    public void displayTable(String department){
        try {
            String sql="SELECT * FROM users WHERE department=? AND user_role='Student'";
            PreparedStatement pst=con.prepareStatement(sql);
            pst.setString(1, department);
            ResultSet rs=pst.executeQuery();
            
            ResultSetMetaData rsd = (ResultSetMetaData) rs.getMetaData();
            int c = rsd.getColumnCount();
            DefaultTableModel d = (DefaultTableModel)students.getModel();
            d.setRowCount(0);
            
              while(rs.next())
            {
            
           Vector v2 = new Vector();
            
            
           for(int i=1; i<=c; i++)
           {
               v2.add(rs.getString("id"));
               v2.add(rs.getString("f_name"));
               v2.add(rs.getString("l_name"));
               v2.add(rs.getString("dob"));
               v2.add(rs.getString("gender"));
               v2.add(rs.getString("nic"));
               v2.add(rs.getString("address"));
               v2.add(rs.getString("contact"));
               v2.add(rs.getString("email"));
               v2.add(rs.getString("image"));
           } 
               
           d.addRow(v2);
           
               
            }
            
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, e);
        }
    }
    
    
